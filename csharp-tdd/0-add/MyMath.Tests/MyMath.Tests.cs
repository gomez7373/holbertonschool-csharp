using System;

namespace MyMath
{
    /// <summary>
    /// Operations class
    /// </summary>
    public class Operations
    {
        /// <summary> add two integers </summary>
        /// <param name="a">first integer</param>
        /// <param name="b">second integer</param>
        /// <returns>sum of two integers</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}