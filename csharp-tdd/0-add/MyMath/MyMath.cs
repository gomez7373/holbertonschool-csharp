namespace MyMath
{
    /// <summary>
    /// Contains mathematical operations.
    /// </summary>
    public static class Operations
    {
        /// <summary>
        /// Adds two integers.
        /// </summary>
        /// <param name="a">First integer.</param>
        /// <param name="b">Second integer.</param>
        /// <returns>Sum of a and b.</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
